@OptIn(ExperimentalMaterialApi::class)
@Composable
fun DarkMode() {
    var isDarkMode by remember { mutableStateOf(false) }

    Column(
        modifier = Modifier
            .padding(horizontal = 14.dp)
            .padding(top = 10.dp)
    ) {
        Text(
            text = "DarkMode",
            fontFamily = Poppins,
            color = SecondaryColor,
            fontSize = 14.sp,
            fontWeight = FontWeight.Bold,
            modifier = Modifier
                .padding(vertical = 8.dp)
        )
        DarkModeSetting(
            icon = R.drawable.ic_rounded_notification,
            mainText = "DarkMode",
            isDarkModeEnabled = isDarkMode,
            onToggleDarkMode = { isDarkMode = it }
        )
    }
}




@OptIn(ExperimentalMaterialApi::class)
@Composable
fun DarkModeSetting(
    icon: Int,
    mainText: String,
    isDarkModeEnabled: Boolean,
    onToggleDarkMode: (Boolean) -> Unit
) {
    val backgroundColor = if (isDarkModeEnabled) Color.Black else Color.White
    val textColor = if (isDarkModeEnabled) Color.White else SecondaryColor

    Card(
        backgroundColor = backgroundColor,
        modifier = Modifier
            .padding(bottom = 8.dp)
            .fillMaxWidth(),
        elevation = 0.dp,
    ) {
        Row(
            modifier = Modifier.padding(vertical = 10.dp, horizontal = 14.dp),
            verticalAlignment = Alignment.CenterVertically,
            horizontalArrangement = Arrangement.SpaceBetween
        ) {
            Row(verticalAlignment = Alignment.CenterVertically) {
                Box(
                    modifier = Modifier
                        .size(34.dp)
                        .clip(shape = Shapes.medium)
                        .background(LightPrimaryColor)
                ) {
                    Icon(
                        painter = painterResource(id = icon),
                        contentDescription = "",
                        tint = Color.Unspecified,
                        modifier = Modifier.padding(8.dp)
                    )
                }

                Spacer(modifier = Modifier.width(14.dp))
                Row(
                    modifier = Modifier.offset(y = (2).dp)
                ) {
                    Text(
                        text = mainText,
                        fontFamily = Poppins,
                        color = textColor,
                        fontSize = 14.sp,
                        fontWeight = FontWeight.Bold,
                    )
                    Spacer(modifier = Modifier.width(8.dp))
                    Button(
                        onClick = { onToggleDarkMode(!isDarkModeEnabled) },
                        modifier = Modifier
                            .padding(start = 8.dp)
                            .size(100.dp, 40.dp),
                        colors = ButtonDefaults.buttonColors(
                            backgroundColor = if (isDarkModeEnabled) PrimaryColor else SecondaryColor,
                            contentColor = if (isDarkModeEnabled) SecondaryColor else PrimaryColor
                        )
                    ) {
                        Text(
                            text = if (isDarkModeEnabled) "Light" else "Dark",
                            fontFamily = Poppins,
                            fontSize = 12.sp,
                            fontWeight = FontWeight.Bold
                        )
                    }
                }
            }
        }
    }
}

